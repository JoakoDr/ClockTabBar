import Foundation
import UIKit


class ColorStyle
{
    class func tintColor() -> UIColor
    {
        return UIColor.orange
    }
    class func navigationBarTintColor() -> UIColor
    {
        return UIColor(red: 69.0/255.0, green: 73.0/255.0, blue: 76.0/255.0, alpha: 1)
    }
}
